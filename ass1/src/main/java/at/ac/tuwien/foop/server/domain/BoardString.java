package at.ac.tuwien.foop.server.domain;

public class BoardString {
	public final String board;
	public final int width;

	public BoardString(String board, int width) {
		this.board = board;
		this.width = width;
	}

	public String getBoard() {
		return board;
	}

	public int getWidth() {
		return width;
	}
	
	
}
